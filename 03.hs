at :: Grid -> (Int,Int) -> Int
at (Grid grid _ width) (x,y) = grid !! (y * width + (x `mod` width))

steps :: Step -> Int -> [(Int,Int)]
steps (Step dx dy) n = [(dx * i, dy * i) | i <- [0..n-1]]

rbind :: (a -> b -> c) -> b -> a -> c
rbind fn x y = fn y x

countTrees :: Context -> Int
countTrees (Context cgrid cstep) = sum . map (at cgrid) . rbind steps gridHeight $ cstep where
  gridHeight = height cgrid

